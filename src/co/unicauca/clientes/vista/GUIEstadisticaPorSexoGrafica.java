    public void graficar(int totalHombres, int totalMujeres) {
        double varPorHombres;
        double varPorMujeres;
        double vartoPersonas;
        
        DecimalFormat formato = new DecimalFormat("#.##");
        
        vartoPersonas = totalHombres + totalMujeres;
        
        varPorHombres = (totalHombres/vartoPersonas)*100;
        varPorMujeres = (totalMujeres/vartoPersonas)*100;
        
        jlbHombres.setText(formato.format(varPorHombres)+"%");
        jPBHombres.setValue((int)Math.round(varPorHombres));
        
        jlbMujeres.setText(formato.format(varPorMujeres)+"%");
        jPBMujeres.setValue((int)Math.round(varPorMujeres));
    }
    
    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres());
    }
